    std::string error;
    BOOST_CHECK_MESSAGE(
            KICHAD::FABRICATION_ARTIFACT_VALIDATOR::ValidateBoardStatistics(
                    fixture.Write( "valid.json", valid.dump( 2 ) + "\n" ),
                    "controller", error ),
            error );

    error.clear();
